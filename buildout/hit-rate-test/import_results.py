#!/usr/bin/env python3
"""
import_results.py - close the loop. results.jsonl -> live organizations table.

Reads a day's discovery results and emits ONE idempotent .sql file that:
  1. writes the found website onto the org (only if the org has no website yet, so
     claimed / owner-submitted sites are never clobbered),
  2. stamps last_verified_at,
  3. seeds camp_scan_queue so the existing camp-scan worker visits the site and
     detects programs (INSERT OR IGNORE, keyed on org_id, so re-runs are safe).

It does NOT touch the database itself. It writes SQL you apply with wrangler, the
same pattern as ingest_irs_bmf.py. Low-confidence and flagged rows are held OUT of
the SQL and written to a review CSV instead, so only confident sites go live.

Apply the SQL:
  npx wrangler d1 execute activity-radar --remote --file=buildout/hit-rate-test/out/import-YYYY-MM-DD.sql

USAGE:
  python buildout/hit-rate-test/import_results.py `
    --results buildout/hit-rate-test/out/results.jsonl
"""

import argparse
import csv
import datetime
import json
import os
import re
import uuid

NOW = datetime.datetime.now(datetime.timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

# Deterministic org id, same scheme as scripts/ingest_irs_bmf.py. We RECOMPUTE the
# org_id from name+city+state instead of trusting the agent's org_id field: over a
# long run the model can regenerate the 36-char UUID instead of copying it, which
# would silently break the join. name+city+state are copied reliably, so this is the
# authoritative key. Verified to reproduce the loaded IRS org ids exactly.
_NS = uuid.uuid5(uuid.NAMESPACE_DNS, "activityradar.fieldforge")


def _norm(s):
    return re.sub(r"\s+", " ", (s or "").strip().lower())


def real_org_id(name, city, state):
    if not name or not state:
        return None
    return "org-%s" % uuid.uuid5(_NS, "nck|%s|%s|%s" % (_norm(name), _norm(city), _norm(state)))


def sql_str(v):
    if v is None or v == "":
        return "NULL"
    return "'" + str(v).replace("'", "''") + "'"


def main():
    ap = argparse.ArgumentParser()
    here = os.path.dirname(__file__)
    ap.add_argument("--results", default=os.path.join(here, "out", "results.jsonl"))
    ap.add_argument("--out-sql", default="")
    ap.add_argument("--review", default="")
    ap.add_argument("--min-confidence", type=int, default=75)
    args = ap.parse_args()

    if not os.path.exists(args.results):
        raise SystemExit("No results at %s. Run a discovery session first." % args.results)

    today = datetime.date.today().isoformat()
    out_sql = args.out_sql or os.path.join(here, "out", "import-%s.sql" % today)
    review = args.review or os.path.join(here, "out", "review-%s.csv" % today)
    os.makedirs(os.path.dirname(os.path.abspath(out_sql)), exist_ok=True)

    accepted, held = [], []
    seen = set()
    with open(args.results, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            r = json.loads(line)
            # Recompute the authoritative org_id from name+city+state; ignore the
            # agent-supplied org_id (it may be a hallucinated UUID over long runs).
            oid = real_org_id(r.get("name"), r.get("city"), r.get("state"))
            if not oid or oid in seen:
                continue          # skip rows with no name and dup orgs
            seen.add(oid)
            r["org_id"] = oid     # use the real id downstream
            url = (r.get("website_url") or "").strip()
            conf = int(r.get("website_confidence") or 0)
            flagged = bool(r.get("needs_review"))
            if url and conf >= args.min_confidence and not flagged:
                accepted.append(r)
            else:
                held.append(r)

    lines = [
        "-- import-%s.sql  (generated by import_results.py)" % today,
        "-- Discovery websites -> organizations + camp_scan_queue. Idempotent.",
        "-- Apply: npx wrangler d1 execute activity-radar --remote --file=%s" % out_sql,
        "",
    ]
    for r in accepted:
        oid = r["org_id"]
        url = r["website_url"].strip()
        lines.append(
            "UPDATE organizations SET website_url=%s, "
            "last_verified_at=%s, updated_at=%s WHERE id=%s "
            "AND (website_url IS NULL OR website_url='');" % (
                sql_str(url), sql_str(NOW), sql_str(NOW), sql_str(oid)))
        # FK-safe: only queue if the org actually exists, so a recomputed id that
        # is not in the table is skipped instead of aborting the whole push.
        lines.append(
            "INSERT OR IGNORE INTO camp_scan_queue (id, org_id, website_url, status, created_at) "
            "SELECT %s, %s, %s, 'pending', %s "
            "WHERE EXISTS (SELECT 1 FROM organizations WHERE id=%s);" % (
                sql_str("csq-" + oid), sql_str(oid), sql_str(url), sql_str(NOW), sql_str(oid)))
    lines.append("")

    with open(out_sql, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    if held:
        with open(review, "w", encoding="utf-8", newline="") as f:
            cols = ["org_id", "name", "city", "state", "engine", "website_url",
                    "social_url", "website_confidence", "needs_review",
                    "website_discovery_reason"]
            w = csv.DictWriter(f, fieldnames=cols)
            w.writeheader()
            for r in held:
                w.writerow({c: r.get(c, "") for c in cols})

    print("results=%d  accepted=%d  held_for_review=%d" % (len(seen), len(accepted), len(held)))
    print("sql    -> %s" % out_sql)
    if held:
        print("review -> %s" % review)
    print("note: website_domain is only set if present; UPDATE skips orgs that already have a site.")


if __name__ == "__main__":
    main()
