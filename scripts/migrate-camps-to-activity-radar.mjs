#!/usr/bin/env node
// migrate-camps-to-activity-radar.mjs
//
// Reads every row from the old parent-coach-playbook D1 and generates a SQL file
// that inserts them into the shared activity-radar D1 (organizations + programs).
//
// Each flat camp row becomes:
//   1 organizations INSERT  (org_id = new UUID, slug = "org-{camp.id}")
//   1 programs INSERT       (id = camp.id, so all existing slugs and URLs stay live)
//
// pcd_status maps from camp.status:
//   'approved' → pcd_status='approved', record_status='active'
//   'pending'  → pcd_status='pending',  record_status='unverified'
//   'rejected' → pcd_status='rejected', record_status='inactive'
//
// Usage:
//   1. Export the old D1 to a JSON file:
//        cd <parent-coach-playbook root>
//        npx wrangler d1 export parent-coach-playbook --remote --output=./scripts/old-camps-export.json
//   2. Run this script:
//        node scripts/migrate-camps-to-activity-radar.mjs
//      Output: scripts/migrate-camps-to-activity-radar.sql
//   3. Apply the migration SQL to the new DB (from the ActivityRadar repo root):
//        npx wrangler d1 execute activity-radar --file=<path>/migrate-camps-to-activity-radar.sql --remote
//
// Safe to run multiple times — both INSERTs use OR IGNORE so re-runs are idempotent.

import { readFileSync, writeFileSync } from 'fs';
import { randomUUID } from 'crypto';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const INPUT  = join(__dirname, 'old-camps-export.json');
const OUTPUT = join(__dirname, 'migrate-camps-to-activity-radar.sql');

// ---------- helpers ----------

function esc(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? '1' : '0';
  // Escape single quotes inside the string.
  return `'${String(v).replace(/'/g, "''")}'`;
}

function recordStatus(pcdStatus) {
  if (pcdStatus === 'approved') return 'active';
  if (pcdStatus === 'rejected') return 'inactive';
  return 'unverified';
}

function confidenceScore(c) {
  if (c === 'high') return 80;
  if (c === 'low')  return 20;
  return 50;
}

// ---------- load export ----------

let raw;
try {
  raw = readFileSync(INPUT, 'utf8');
} catch {
  // Try UTF-16 LE (wrangler d1 export sometimes emits this).
  try {
    raw = readFileSync(INPUT);
    raw = raw.toString('utf16le');
  } catch (e) {
    console.error(`Cannot read ${INPUT}: ${e.message}`);
    console.error('Run: npx wrangler d1 export parent-coach-playbook --remote --output=./scripts/old-camps-export.json');
    process.exit(1);
  }
}

// Wrangler d1 export JSON has shape: { results: [ {...camp row...}, ... ] }
// or a raw array depending on the export flag used.
let camps;
try {
  const parsed = JSON.parse(raw.trim());
  camps = Array.isArray(parsed) ? parsed : (parsed.results ?? parsed);
} catch (e) {
  console.error(`JSON parse failed: ${e.message}`);
  process.exit(1);
}

if (!Array.isArray(camps) || camps.length === 0) {
  console.error('No camp rows found in export. Check the file format.');
  process.exit(1);
}

console.log(`Found ${camps.length} camp rows.`);

// ---------- generate SQL ----------

const lines = [
  '-- migrate-camps-to-activity-radar.sql',
  '-- Auto-generated by migrate-camps-to-activity-radar.mjs',
  `-- Source: ${camps.length} rows from parent-coach-playbook D1`,
  '-- Target: activity-radar D1 (organizations + programs)',
  '-- Apply: npx wrangler d1 execute activity-radar --file=./migrate-camps-to-activity-radar.sql --remote',
  '--',
  '-- Both INSERTs use OR IGNORE — safe to re-run.',
  '',
];

let skipped = 0;
let written = 0;

for (const c of camps) {
  // Skip rows that are clearly test/seed data with no real name.
  if (!c.id || !c.slug || !c.name) { skipped++; continue; }

  const orgId  = randomUUID();
  const now    = c.submitted_at ?? new Date().toISOString();
  const pcd    = c.status ?? 'pending';
  const rs     = recordStatus(pcd);
  const cs     = confidenceScore(c.confidence);
  const src    = c.source_domain ? 'scraped' : 'manual';

  // organizations INSERT
  lines.push(
    `INSERT OR IGNORE INTO organizations ` +
    `(id, slug, name, organization_type, ` +
    `website_url, email, phone, ` +
    `address, city, state, zip, latitude, longitude, ` +
    `description, ` +
    `record_source, record_status, is_claimed, claimed_by_email, claim_paid_until, ` +
    `logo_key, gallery_keys, ` +
    `confidence_score, ` +
    `created_at, updated_at) VALUES (` +
    [
      esc(orgId),
      esc(`org-${c.id}`),
      esc(c.name),
      `'other'`,
      esc(c.website_url),
      esc(c.contact_email),
      esc(c.contact_phone),
      esc(c.address),
      esc(c.city),
      esc(c.state),
      esc(c.zip),
      esc(c.latitude),
      esc(c.longitude),
      esc((c.description ?? '').slice(0, 500)),
      esc(src),
      esc(rs),
      esc(c.is_claimed ?? 0),
      esc(c.claimed_by_email),
      esc(c.claim_paid_until),
      esc(c.logo_key),
      esc(c.gallery_keys),
      esc(cs),
      esc(now),
      esc(now),
    ].join(', ') +
    ');',
  );

  // programs INSERT
  lines.push(
    `INSERT OR IGNORE INTO programs ` +
    `(id, organization_id, slug, name, ` +
    `program_type, activity_category, ` +
    `description, ` +
    `age_min, age_max, skill_level, ` +
    `session_start_date, session_end_date, ` +
    `price_text, ` +
    `day_or_overnight, lunch_included, aftercare_available, ` +
    `availability_status, ` +
    `registration_url, ` +
    `registration_deadline, schedule_text, ` +
    `contact_email, contact_phone, ` +
    `record_source, record_status, ` +
    `confidence_score, source_domain, ` +
    `hero_photo_key, ` +
    `pcd_status, submitted_by_email, submitted_at, ` +
    `reviewed_by, reviewed_at, review_notes, reject_reason_code, ` +
    `verified, awaiting_review, featured, featured_order, featured_until, ` +
    `url_health_status, url_last_checked_at, url_last_status_code, ` +
    `last_edited_at, last_edited_by, ` +
    `pcd_confidence, ` +
    `legacy_camp_id, legacy_slug, ` +
    `created_at, updated_at) VALUES (` +
    [
      esc(c.id),
      esc(orgId),
      esc(c.slug),
      esc(c.name),
      esc(c.program_type ?? 'camp'),
      esc(c.sport),                         // activity_category
      esc(c.description),
      esc(c.age_min),
      esc(c.age_max),
      esc(c.skill_level ?? 'all'),
      esc(c.start_date),                    // session_start_date
      esc(c.end_date),                      // session_end_date
      esc(c.price_text),
      esc(c.day_or_overnight ?? 'day'),
      esc(c.lunch_included ?? 0),
      esc(c.aftercare_available ?? 0),
      esc(c.spots_status ?? 'open'),        // availability_status
      esc(c.registration_url ?? c.website_url),
      esc(c.registration_deadline),
      esc(c.schedule_text),
      esc(c.contact_email),
      esc(c.contact_phone),
      esc(src),
      esc(rs),
      esc(cs),
      esc(c.source_domain),
      esc(c.hero_photo_key),
      esc(pcd),                             // pcd_status
      esc(c.submitted_by_email ?? 'system'),
      esc(c.submitted_at ?? now),
      esc(c.reviewed_by),
      esc(c.reviewed_at),
      esc(c.review_notes),
      esc(c.reject_reason_code),
      esc(c.verified ?? 0),
      esc(c.awaiting_review ?? 0),
      esc(c.featured ?? 0),
      esc(c.featured_order),
      esc(c.featured_until),
      esc(c.url_health_status ?? 'unchecked'),
      esc(c.url_last_checked_at),
      esc(c.url_last_status_code),
      esc(c.last_edited_at),
      esc(c.last_edited_by),
      esc(c.confidence ?? 'medium'),        // pcd_confidence
      esc(c.id),                            // legacy_camp_id (self-reference for crosswalk)
      esc(c.slug),                          // legacy_slug
      esc(now),
      esc(now),
    ].join(', ') +
    ');',
  );

  lines.push('');
  written++;
}

writeFileSync(OUTPUT, lines.join('\n'), 'utf8');

console.log(`Done. ${written} camps written, ${skipped} skipped.`);
console.log(`Output: ${OUTPUT}`);
console.log('');
console.log('Next step — apply to activity-radar D1 (run from ActivityRadar repo root):');
console.log(`  npx wrangler d1 execute activity-radar --file="${OUTPUT}" --remote`);
